
public class Pessoa {

	public String nome;
	public String endereco;
	public String email;
	
	
	

	/**
	 * Get nome.
	 * @return
	 */
	public String getNome() {
		return nome;
	}

	/**
	 * Set nome.
	 * @param nome
	 */
	public void setNome(String nome) {
		this.nome = nome;
	}

	/**
	 * Get endereco.
	 * @return
	 */
	public String getEndereco() {
		return endereco;
	}

	/**
	 * Set endereco.
	 * @param endereco
	 */
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}

	/**
	 * Get email.
	 * @return
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * Set email.
	 * @param email
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	
	
	
}
